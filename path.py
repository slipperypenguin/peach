def goTo(a, b):
    dx = round(b[0] - a[0], 6)
    dy = round(b[1] - a[1], 6)

    sx = 1
    sy = 1

    magDx = abs(dx)
    magDy = abs(dy)

    if magDx == 0:
        d = magDy
    elif magDy == 0:
        d = magDx
    if magDx == magDy:
        d = magDx
    elif(magDx > magDy):
        d = magDx
        sy = magDy/magDx
    elif(magDx < magDy):
        d = magDy
        sx = magDx/magDy
    
    moveHorz = Thread(target=move, args=(d, -dx, sx, horizontal))
    moveHorz.start()

    moveVert = Thread(target=move, args=(d, -dy, sy, vertical))
    moveVert.start()

    moveHorz.join()
    moveVert.join()

